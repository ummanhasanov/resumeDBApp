package com.mycompany.entity;

/**
 *
 * @author Umman Hasan
 */
public class Country
{
    private int id;
    private String nationality;
    private String name;

    public Country() {
    }

    public Country(int id, String nationality, String name) {
        this.id = id;
        this.nationality = nationality;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNationality() {
        return nationality;
    }

    public void setNationality(String nationality) {
        this.nationality = nationality;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Country{" + "id=" + id + ", nationality=" + nationality + ", name=" + name + '}';
    }
    
    
}
